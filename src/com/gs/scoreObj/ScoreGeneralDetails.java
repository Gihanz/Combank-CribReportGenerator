package com.gs.scoreObj;

public class ScoreGeneralDetails {
	
	private String reportOrderNumber;
	private String reportOrderdate;
	
	/**
	 * @return the reportOrderNumber
	 */
	public String getReportOrderNumber() {
		return reportOrderNumber;
	}
	/**
	 * @param reportOrderNumber the reportOrderNumber to set
	 */
	public void setReportOrderNumber(String reportOrderNumber) {
		this.reportOrderNumber = reportOrderNumber;
	}
	/**
	 * @return the reportOrderdate
	 */
	public String getReportOrderdate() {
		return reportOrderdate;
	}
	/**
	 * @param reportOrderdate the reportOrderdate to set
	 */
	public void setReportOrderdate(String reportOrderdate) {
		this.reportOrderdate = reportOrderdate;
	}
}
