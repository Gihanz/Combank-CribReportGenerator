package com.gs.scoreObj;

public class CribScore {
	
	private String riskGrade;
	private String cribScore;
	private String riskGradeColor;
	
	/**
	 * @return the riskGradeColor
	 */
	public String getRiskGradeColor() {
		return riskGradeColor;
	}
	/**
	 * @param riskGradeColor the riskGradeColor to set
	 */
	public void setRiskGradeColor(String riskGradeColor) {
		this.riskGradeColor = riskGradeColor;
	}
	/**
	 * @return the riskGrade
	 */
	public String getRiskGrade() {
		return riskGrade;
	}
	/**
	 * @param riskGrade the riskGrade to set
	 */
	public void setRiskGrade(String riskGrade) {
		this.riskGrade = riskGrade;
	}
	/**
	 * @return the cribScore
	 */
	public String getCribScore() {
		return cribScore;
	}
	/**
	 * @param cribScore the cribScore to set
	 */
	public void setCribScore(String cribScore) {
		this.cribScore = cribScore;
	}	
}