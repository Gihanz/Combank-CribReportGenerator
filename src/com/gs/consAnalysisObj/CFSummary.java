package com.gs.consAnalysisObj;

public class CFSummary {
		
	private String typeOfLiabilities;
	private String totalNumberOfFacilities;
	private String totalAmountGrantedLimit;
	private String totalOutstanding;
	
	/**
	 * @return the typeOfLiabilities
	 */
	public String getTypeOfLiabilities() {
		return typeOfLiabilities;
	}
	/**
	 * @param typeOfLiabilities the typeOfLiabilities to set
	 */
	public void setTypeOfLiabilities(String typeOfLiabilities) {
		this.typeOfLiabilities = typeOfLiabilities;
	}
	/**
	 * @return the totalNumberOfFacilities
	 */
	public String getTotalNumberOfFacilities() {
		return totalNumberOfFacilities;
	}
	/**
	 * @param totalNumberOfFacilities the totalNumberOfFacilities to set
	 */
	public void setTotalNumberOfFacilities(String totalNumberOfFacilities) {
		this.totalNumberOfFacilities = totalNumberOfFacilities;
	}
	/**
	 * @return the totalAmountGrantedLimit
	 */
	public String getTotalAmountGrantedLimit() {
		return totalAmountGrantedLimit;
	}
	/**
	 * @param totalAmountGrantedLimit the totalAmountGrantedLimit to set
	 */
	public void setTotalAmountGrantedLimit(String totalAmountGrantedLimit) {
		this.totalAmountGrantedLimit = totalAmountGrantedLimit;
	}
	/**
	 * @return the totalOutstanding
	 */
	public String getTotalOutstanding() {
		return totalOutstanding;
	}
	/**
	 * @param totalOutstanding the totalOutstanding to set
	 */
	public void setTotalOutstanding(String totalOutstanding) {
		this.totalOutstanding = totalOutstanding;
	}

}
